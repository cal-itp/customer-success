    DEALS = auto()
    TICKETS = auto()
    VENDORS = "2-22517187"  # vendors objectTypeId value
    CALLS = auto()
    EMAILS = auto()
    MEETINGS = auto()
    NOTES = auto()
    TASKS = auto()


