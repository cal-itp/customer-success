    slug: str
    core_props: list[str]
    extra_props: list[str] = field(default_factory=list)
    body_props: list[str] = field(default_factory=list)
    # Each sublist of associations can only contain four items due to HubSpot export API limitations.
    associations: list[list[str]] = field(default_factory=list)


